public class KontakObject {

    private String nama, id, status, urlFotoKontak;


    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getNama() {
        return nama;
    }

    public void setNama(String nama) {
        this.nama = nama;
    }

    public String getStatus() {
        return status;
    }

    public void setStatus(String status) {
        this.status = status;
    }

    public String getUrlFotoKontak() {
        return urlFotoKontak;
    }

    public void setUrlFotoKontak(String urlFotoKontak) {
        this.urlFotoKontak = urlFotoKontak;
    }
}
